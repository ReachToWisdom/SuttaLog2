const isV = ch => ch in VOWELS

function convert(roman) {
  let r = '', i = 0
  const s = roman.toLowerCase()
  while (i < s.length) {
    const ch = s[i]
    if (/[\s,.;:!?\-\/]/.test(ch)) { r += ch === '/' ? ' ' : ch; i++; continue }
    if (ch === 'ṃ') { r += '응'; i++; continue }
    const three = s.slice(i, i + 3), two = s.slice(i, i + 2)
    let con = null, consumed = 0
    if (CONS[three]) { con = three; consumed = 3 }
    else if (CONS[two]) { con = two; consumed = 2 }
    else if (CONS[ch]) { con = ch; consumed = 1 }
    if (con) {
      i += consumed
      if (i < s.length && isV(s[i])) {
        r += COMBO[con]?.[s[i]] || CONS[con][0]
        i++
      } else {
        r += CONS[con][1]
      }
      continue
    }
    if (isV(ch)) { r += VOWELS[ch]; i++; continue }
    r += ch; i++
  }
  return r
}

